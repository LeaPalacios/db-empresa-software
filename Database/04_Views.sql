-- (VW_RESUMENFINANCIEROCLIENTES)PERMITE ANALIZAR QUÉ CLIENTES GENERAN MAYOR FACTURACIÓN POR CONSULTORÍA.

CREATE VIEW VW_RESUMENFINANCIEROCLIENTES AS
SELECT 
    EC.ID_EMPRESA_CLIENTE AS ID,
    EC.CIF_EMPRESA AS CIF,
    EC.NOMBRE_EMPRESA AS CLIENTE,
    COUNT(DISTINCT P.ID_PROYECTO) AS CANTIDAD_PROYECTOS,
    ISNULL(SUM(DP.HORAS_TRABAJADAS), 0) AS TOTAL_HORAS_INVERTIDAS,
    CAST(ISNULL(SUM(DP.HORAS_TRABAJADAS * DP.COSTO_HORA), 0) AS DECIMAL(12,2)) AS TOTAL_INVERSION_REALIZADA
FROM EMPRESA_CLIENTE EC
LEFT JOIN PROYECTO P ON EC.ID_EMPRESA_CLIENTE = P.ID_EMPRESA_CLIENTE
LEFT JOIN DETALLE_PROYECTO DP ON P.ID_PROYECTO = DP.ID_PROYECTO
GROUP BY EC.ID_EMPRESA_CLIENTE, EC.CIF_EMPRESA, EC.NOMBRE_EMPRESA;

