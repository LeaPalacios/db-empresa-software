-- (VW_CONSOLIDADOPROYECTOS) CONSOLIDA LAS HORAS TRABAJADAS, COSTO TOTAL Y ESTADO ACTUAL POR PROYECTO.

CREATE VIEW VW_CONSOLIDADOPROYECTOS AS
SELECT 
    P.ID_PROYECTO AS CODIGO_PROYECTO,
    P.NOMBRE_PROYECTO AS PROYECTO,
    EC.NOMBRE_EMPRESA AS CLIENTE,
    EP.NOMBRE_ESTADO AS ESTADO,
    P.FECHA_INICIO AS FECHA_INICIO,
    ISNULL(SUM(DP.HORAS_TRABAJADAS), 0) AS TOTAL_HORAS_TRABAJADAS,
    -- CALCULAMOS EL COSTO TOTAL DEL PROYECTO MULTIPLICANDO HORAS POR COSTO DE HORA CONGELADO
    CAST(ISNULL(SUM(DP.HORAS_TRABAJADAS * DP.COSTO_HORA), 0) AS DECIMAL(12,2)) AS COSTO_TOTAL_ACUMULADO
FROM PROYECTO P
INNER JOIN EMPRESA_CLIENTE EC ON P.ID_EMPRESA_CLIENTE = EC.ID_EMPRESA_CLIENTE
INNER JOIN ESTADO_PROYECTO EP ON P.ID_ESTADO = EP.ID_ESTADO
LEFT JOIN DETALLE_PROYECTO DP ON P.ID_PROYECTO = DP.ID_PROYECTO
GROUP BY P.ID_PROYECTO, P.NOMBRE_PROYECTO, EC.NOMBRE_EMPRESA, EP.NOMBRE_ESTADO, P.FECHA_INICIO;
GO

