--  (VW_ORGANIGRAMAEMPLEADOS) MUESTRA EL DEPARTAMENTO, PROFESIÓN Y EL NOMBRE DEL JEFE DIRECTO (SELF-JOIN)

CREATE VIEW VW_ORGANIGRAMAEMPLEADOS AS
SELECT 
    E.ID_EMPLEADO AS ID,
    E.DNI_EMPLEADO AS DNI,
    CONCAT(E.NOMBRE_EMPLEADO, ' ', E.APELLIDO_EMPLEADO) AS COLABORADOR,
    P.NOMBRE_PROFESION AS PROFESION,
    D.NOMBRE_DEPARTAMENTO AS DEPARTAMENTO,
    -- SELF-JOIN PARA TRAER EL NOMBRE DEL JEFE USANDO ID_JEFE -> ID_EMPLEADO
    CASE 
        WHEN E.ID_JEFE IS NULL THEN 'MÁXIMO RANGO / SIN JEFE DIRECTO'
        ELSE CONCAT(J.NOMBRE_EMPLEADO, ' ', J.APELLIDO_EMPLEADO)
    END AS JEFEDIRECTO
FROM EMPLEADO E
INNER JOIN PROFESION P ON E.ID_PROFESION = P.ID_PROFESION
INNER JOIN DEPARTAMENTO D ON E.ID_DEPARTAMENTO = D.ID_DEPARTAMENTO
LEFT JOIN EMPLEADO J ON E.ID_JEFE = J.ID_EMPLEADO; -- LEFT JOIN PORQUE EL DIRECTOR NO TIENE JEFE
GO

